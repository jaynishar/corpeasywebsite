<h2 class="text-3xl font-bold text-slate-900 mb-6">The Hidden Charges Most Providers Do Not Mention</h2>
<p class="text-slate-600 leading-relaxed mb-6">If you are comparing base rents across providers, be aware that the actual cost of a traditional office lease is typically <strong>20–35% higher</strong> than the quoted rent due to these additional charges:</p>
<div class="space-y-4 mb-8">
<div class="glass-card p-6">
<h3 class="text-lg font-bold text-slate-900 mb-2">CAM (Common Area Maintenance)</h3>
<p class="text-slate-600 leading-relaxed">₹25–₹60 per sq ft/month. Covers lobby maintenance, elevator service, common area electricity, and building security. Often quoted separately from base rent.</p>
</div>
<div class="glass-card p-6">
<h3 class="text-lg font-bold text-slate-900 mb-2">GST on Rent</h3>
<p class="text-slate-600 leading-relaxed">18% GST applies to all commercial rentals in India. This is calculated on the total rent (base + CAM), so on a ₹300/sq ft rent, you are paying an additional ₹54/sq ft in GST.</p>
</div>
<div class="glass-card p-6">
<h3 class="text-lg font-bold text-slate-900 mb-2">Parking</h3>
<p class="text-slate-600 leading-relaxed">₹3,000–₹8,000 per car/month in premium areas. BKC and Lower Parel parking can go up to ₹12,000/month per car. This is rarely included in the quoted rent.</p>
</div>
<div class="glass-card p-6">
<h3 class="text-lg font-bold text-slate-900 mb-2">Interior Setup (One-Time)</h3>
<p class="text-slate-600 leading-relaxed">₹800–₹1,500 per sq ft for basic interiors (partitions, flooring, electrical, lighting). For a 2,000 sq ft office, that is ₹16–₹30 lakhs upfront — before you even buy furniture.</p>
</div>
<div class="glass-card p-6">
<h3 class="text-lg font-bold text-slate-900 mb-2">Furniture</h3>
<p class="text-slate-600 leading-relaxed">₹50,000–₹1,00,000 per workstation for a decent ergonomic chair, desk, and storage. A 30-person office needs ₹15–₹30 lakhs in furniture alone.</p>
</div>
